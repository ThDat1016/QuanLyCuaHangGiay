        BigDecimal giaBan = new BigDecimal(DonGia.getText());

        e.setiSoLuong((int) SoLuong.getValue());
        BigDecimal soLuong = new BigDecimal((int) SoLuong.getValue());
        Memory.giaban = giaBan.multiply(soLuong);
        e.setiGia(Memory.giaban);
