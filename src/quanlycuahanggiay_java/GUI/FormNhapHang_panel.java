        e.getThuonghieu().setMaThuongHieu(maThuongHieu);

        // add chi tiet PN
        ctpn.setGiaNhap(Memory.giaban);
        ctpn.getGiay().setStrMaGiay(0); // luc nhap hang se cap nhat lai ma giay
        ctpn.getPn().setMaPN(0);    // luc nhap hang se cap nhat lai ma PN
        ctpn.getGiay().setiSoLuong((int) SoLuong.getValue());

        chiTietPN.add(ctpn);
        list.add(e);
        try {
            DefaultTableModel model = (DefaultTableModel) ChiTiet.getModel();
            Object[] rowData = {e.getStrTenGiay(), e.getiSoLuong(), e.getiGia()};
            model.addRow(rowData);
            Memory.tongTien.add(Memory.giaban);
              
        } catch (Exception ex) {
            System.out.println("Lỗi xảy ra: " + ex.getMessage());
        }
        
        TenGiay.setText("");
        SoLuong.setValue(0);
        DonGia.setText("");
        Size.setText("");
        ChatLieu.setText("");
        TenGiay.requestFocus();
