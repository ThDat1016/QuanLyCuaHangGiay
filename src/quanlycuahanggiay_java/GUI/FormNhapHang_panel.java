    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        try {
            int rowIndex = ChiTiet.getSelectedRow();
            if (rowIndex != -1) {
                DefaultTableModel model = (DefaultTableModel) ChiTiet.getModel();
                model.removeRow(rowIndex);
                list.remove(rowIndex);
                chiTietPN.remove(rowIndex);
                Memory.tongTien.remove(rowIndex);
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_deleteMouseClicked

    private void NhapHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_NhapHangMouseClicked

        try {
            PhieuNhap pn = new PhieuNhap();

            ConnectUnit conn = new ConnectUnit();
            PhieuNhapDAO pnDAO = new PhieuNhapDAO();
            ChiTietPNDAO chiTietPNDAO = new ChiTietPNDAO();
            GiayBUS giayBUS = new GiayBUS();
            GiayDAO giayDAO = new GiayDAO();

            //them phieu nhap vao db
            LocalDateTime ngayHienTai = LocalDateTime.now();
            java.sql.Date sqlDate = java.sql.Date.valueOf(ngayHienTai.toLocalDate());
            pn.setNgayNhap(sqlDate);
            pn.getNv().setMaNV(Memory.nhanvien.getMaNV());
            Object selectedObject = MaNCC.getSelectedItem();
            String mancc = (String) selectedObject;
            pn.getNCC().setMaNCC(mancc);
            BigDecimal sum = BigDecimal.ZERO; // Khởi tạo tổng ban đầu là 0
            for (BigDecimal value : Memory.tongTien) {
                sum = sum.add(value); // Cộng giá trị hiện tại vào tổng
            }
            pn.setTongTien(sum);

            if (pnDAO.them(pn)) {
                System.out.println("ok");
            } else {
                System.out.println("no");
            }

            //them chi tiet phieu nhap vao db
            for (int i = 0; i < list.size(); i++) {
                Giay giay = list.get(i);
                ChiTietPhieuNhap ct = chiTietPN.get(i);

                // Thực hiện xử lý với mỗi phần tử trong danh sách
                if (giayDAO.them(giay)) {
                    System.out.println("Thêm giày thành công");
                } else {
                    System.out.println("Không thể thêm giày");
                }

                // Gán mã giày cho chi tiết phiếu nhập
                ct.getGiay().setStrMaGiay(conn.getMaxMaGiay());
                ct.getPn().setMaPN(conn.getMaxMaPN());
                if (chiTietPNDAO.them(ct)) {
                    System.out.println("ok");
                } else {
                    System.out.println("no");
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());

        } finally {
            MainFrame1 mf = (MainFrame1) SwingUtilities.getWindowAncestor(this);

            // Tạo panel mới HoaDonGUI_panel
            NhapHang_panel hdp = new NhapHang_panel();

            // Thêm panel mới vào Body_panel của MainFrame1
            mf.getBody_panel().add(hdp, "NhapHang_panel");

            // Sử dụng CardLayout để chuyển đến HoaDonGUI_panel
            CardLayout cl = (CardLayout) (mf.getBody_panel().getLayout());
            cl.show(mf.getBody_panel(), "NhapHang_panel");

            // Cập nhật giao diện người dùng
            mf.getBody_panel().revalidate();
            mf.getBody_panel().repaint();
        }
    }//GEN-LAST:event_NhapHangMouseClicked

